    public void setAnhData(byte[] anhData) {
        this.anhData = anhData;
        if (anhData != null && anhData.length > 0) {
            try {
                ByteArrayInputStream bais = new ByteArrayInputStream(anhData);
                BufferedImage originalImage = ImageIO.read(bais);
                if (originalImage == null) {
                    lblAnh.setIcon(null);
                    return;
                }

                int targetWidth = 140;
                int targetHeight = 200;
                double aspectRatio = (double) originalImage.getWidth() / originalImage.getHeight();
                if (aspectRatio > (double) targetWidth / targetHeight) {
                    targetHeight = (int) (targetWidth / aspectRatio);
                } else {
                    targetWidth = (int) (targetHeight * aspectRatio);
                }

                BufferedImage scaledImage = new BufferedImage(targetWidth, targetHeight, BufferedImage.TYPE_INT_ARGB);
                Graphics2D g2d = scaledImage.createGraphics();
                g2d.setRenderingHint(RenderingHints.KEY_INTERPOLATION, RenderingHints.VALUE_INTERPOLATION_BICUBIC);
                g2d.drawImage(originalImage, 0, 0, targetWidth, targetHeight, null);
                g2d.dispose();

                // Căn giữa ảnh trong JLabel
                ImageIcon icon = new ImageIcon(scaledImage);
                lblAnh.setIcon(icon);
                lblAnh.setHorizontalAlignment(SwingConstants.CENTER);
                lblAnh.setVerticalAlignment(SwingConstants.CENTER);
            } catch (Exception e) {
                lblAnh.setIcon(null);
            }
        } else {
            lblAnh.setIcon(null);
        }
    }

